#include "Personaje.h"

///////////////////////////////////////////////////////////////////////////
void Personaje::setup()
{
  // Posicion de inicio
  m_x = 1;
  m_y = 3;
}

///////////////////////////////////////////////////////////////////////////
void Personaje::draw()
{
  mvaddch(m_y, m_x, '0');
}

///////////////////////////////////////////////////////////////////////////
void Personaje::setX(int x) { m_x = x; }
void Personaje::setY(int y) { m_y = y; }

///////////////////////////////////////////////////////////////////////////
int Personaje::getX() { return m_x; }
int Personaje::getY() { return m_y; }